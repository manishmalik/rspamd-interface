nacl.secretbox = function(msg, nonce, key) {
  checkArrayTypes(msg, nonce, key);
  checkLengths(key, nonce);
  var m = new Uint8Array(msg.length);
  var c = new Uint8Array(msg.length + crypto_secretbox_MACBYTES);

  for (var i = 0; i < msg.length; i++) m[i] = msg[i];
  crypto_secretbox(c, m, m.length, nonce, key);
  return c;
};

nacl.secretbox.open = function(box, nonce, key) {
  checkArrayTypes(box, nonce, key);
  checkLengths(key, nonce);
  var c = new Uint8Array(box.length);
  var m = new Uint8Array(box.length - 16);
  for (var i = 0; i < box.length; i++) c[i] = box[i];
  if (crypto_secretbox_open(m, c, c.length, nonce, key) !== 0) return false;
  return m;
};

